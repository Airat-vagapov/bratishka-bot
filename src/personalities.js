const DEFAULT_PERSONALITY = 'bratishka';

/**
 * Возвращает личность по имени или дефолтную, если такой нет.
 * @param {string} [name]
 * @returns {Personality}
 */
function getPersonality(name = DEFAULT_PERSONALITY) {
  const key = (name || DEFAULT_PERSONALITY).toLowerCase();
  return PERSONALITIES[key] || PERSONALITIES[DEFAULT_PERSONALITY];
}

/**
 * Возвращает список доступных личностей.
 * @returns {Personality[]}
 */
function listPersonalities() {
  return Object.values(PERSONALITIES);
}

/**
 * Проверяет, существует ли личность с таким именем.
 * @param {string} name
 * @returns {boolean}
 */
function isValidPersonality(name) {
  return Boolean(name && PERSONALITIES[name.toLowerCase()]);
}

